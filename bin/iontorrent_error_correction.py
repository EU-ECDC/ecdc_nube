import os
import re
from Bio.SeqIO import parse
import argparse
import sys

parser = argparse.ArgumentParser()
parser.add_argument("assembly_file", type=str,
                    help="original assembly")
parser.add_argument("fasta_ref_seqs_alleles", type=str,
                    help="fasta file with the one allele sequence per locus")
parser.add_argument("out_fasta", type=str,
                    help="output assembly")
if len(sys.argv)==1:
    parser.print_help(sys.stderr)
args = parser.parse_args()

dbname = 'blastdb_ref'

# This file should contain example alleles of each locus in the scheme
out_ref_file = args.fasta_ref_seqs_alleles

# SHort indels that indicate Iontorrent-related errors
forbidden_indel_lenghts = [1, 2, 4, 5]

# Simple function for reading a multi-fasta into a dictionary
def load_data(fastafile):
    with open(fastafile, 'r') as ref:
        ref_data = {}
        for record in parse(ref, 'fasta'):
            name = record.name
            seq = record.seq
            ref_data[name] = seq
    return ref_data

    
# Input assembly file
assembly_file = args.assembly_file

# Temporary txt file generated by BLAST
blast_file = "blast_results.txt"

# Filtered assembly
out_file = args.out_fasta

print(assembly_file)
assembly = load_data(assembly_file)
outdata = {}

# Run BLAST of reference alleles against assembly
cmd = 'makeblastdb -dbtype nucl -in {} -out {}'.format(out_ref_file, dbname)
os.system(cmd)
cmd = 'blastn -db {} -query {} -dust no -outfmt \"6 qseqid sseqid btop qstart qend\" -parse_deflines > {}'.format(dbname, assembly_file, blast_file)
os.system(cmd)

c = 0
delete_regions = {}

# Process BLAST output, get all short indels and flag corresponding CDS regions for deletion
with open(blast_file) as bfile:
    for line in bfile:
        fields = line.split('\t')
        discard = False
        if re.search(r'[-]', fields[2]):
            #print(fields[2])
            for m in re.finditer('[A-Z0-9]?([-].){1,20}[-]?',fields[2]):
                #print(m)
                indel_len = m.group(0).count('-')
                #print(indel_len)

                if indel_len in forbidden_indel_lenghts:
                    discard = True
        if discard:
            if fields[0] not in delete_regions:
                delete_regions[fields[0]] = []
            
            qstart = int(fields[3])
            qend = int(fields[4])
            if qstart > qend:
                tmp = qstart
                qstart = qend
                qend = tmp
                
            
            delete_regions[fields[0]].append((qstart, qend))
            c += 1

# Process the assembly, applying all deletions
for contig in assembly:
    seq = assembly[contig]
    if contig in delete_regions:
        print(contig)
        regions = sorted(delete_regions[contig], key=lambda x: x[0])
        print(regions)
        start_pos = 0
        subcontig = 1
        for region in regions:
            end_pos = region[0] - 1
            cutseq = seq[start_pos:end_pos]
            subcontig_name = contig + '_' + str(subcontig)
            outdata[subcontig_name] = cutseq
            subcontig+=1
            start_pos = region[1]
        cutseq = seq[start_pos:]
        subcontig_name = contig + '_' + str(subcontig)
        outdata[subcontig_name] = cutseq
    else:
        outdata[contig] = seq

    
# Write output file
nc = 0
with open(out_file, 'w') as outf:
    for contig in outdata:
        if len(outdata[contig]) > 50:
            nc += 1
            outf.write('>' + contig+'\n')
            outf.write(str(outdata[contig])+'\n')

# Output statistics
print('Number of loci to be trimmed: {}'.format(c))
print('Number of contigs in input file: {}'.format(len(assembly.keys())))
print('Number of sub-contigs in output file: {}'.format(nc))
